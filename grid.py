    def add(self, fields):
        for field in fields:
            x = 0 + field.x
            y = 0 + field.y
            self.grid[y][x] = field.t.value

    def draw(self):
        for line in self.grid:
            for field in line:
                sys.stdout.write(field + "  ")
            print()
